class Algorithm {
public:
    Algorithm( const string& a ):
        n( siz( a )),
        a( a ),
        p( string( n, '-' ))
    {}
    void pusk() {
        srch( 0 );
        CONT = { "", "", "\n" };
        cout << siz( s ) << nl << s << nl;
    }
private:
    const u32 n;
    const string& a;
    string p;
    set<string> s;
    void srch( u32 j ){
        if( j == n ){
            s.insert( p );
        }
        for( u32 i = 0; i < n; ++i ){
            if( p [i] == '-' ){
                p [i] = a [j];
                srch( j + 1 );
                p [i] = '-';
            }
        }
    }
};
