class Algorithm {
public:
    Algorithm( i32 n, i32 x, vi32& p ):
        n( n ),
        x( x ),
        p( p )
    {
        sort( all( p ));
    }
    void pusk() {
        dbg3( n, x, p );
        i32 c = 0;
        i32 i = 0;
        i32 j = n - 1;
        while( i < j ){
            if( p [i] + p [j] <= x ){
                ++i;
            }
            ++c;
            --j;
        }
        if( i == j ){ ++c; }
        cout << c << nl;
    }
private:
    const i32 n;
    const i32 x;
    vi32& p;
};
