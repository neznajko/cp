// In the book «Concrete Mathematics: A Foundation
// for Computer Science» they solve the classical
// Josephus problem, namely what is the last remaining
// person, in a closed form, but the basic idea can be
// used here as well. For example if we start with 2n
// people with n = 4: 1 2 3 4 5 6 7 8, after first
// round all even numbers are knocked out, and we are
// left with 1 3 5 7, which is the 1 2 3 4 problem but
// with renamed numbers: J(2n) = 2J(n) - 1 etc.
//////////////////////////////////////////////////////
i32 J( i32 n, i32 k ){
  if( n & 1 ){ // n = 2n + 1
    n >>= 1;
    if( k <= n ){ return (k << 1); }
    if( k == n + 1 ){ return 1; }
    return (J(n, k - n - 1) << 1) + 1;
  } else { // n = 2n
    n >>= 1;
    if( k <= n ){ return (k << 1); }
    return (J( n, k - n ) << 1) - 1;
  }
}
