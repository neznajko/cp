class Algorithm {
public:
    Algorithm( v<pu32>& t ):
        t( t )
    {
        sort( all( t ));
    }
    void pusk() {
        u32 c = 0;
        u32 m = 0;
        for( const auto& y: t ){
            if( val( y ) == ARIVE ){
                ++c;
                m = max( m, c );
            } else {
                --c;
            }
        }
        cout << m << nl;
    }
private:
    v<pu32>& t;
};
