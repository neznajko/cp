namespace cses {
  constexpr i32 N = 200001;

  i32 tin[N];
  i32 size[N];
  i32 inorder[N];
  i64 v[N];
  i64 pref[N];
  i64 tree[N]; // bit

  i32 n = 0;
  i32 time = 0;
  i32 pos = 0;

  vector<i32> adj[N];
  void dfs( i32 u, i32 t ){
    tin[u] = ++time;
    inorder[u] = ++pos;
    pref[pos] = v[u];

    for( auto v: adj[u] ){
      if( v == t ){ continue; }
      dfs( v, u );
    }

    size[u] = ((++time) - tin[u] + 1)/ 2;
  }
  i64 sum( i32 k ){
    i64 s = 0;
    while( k > 0 ){
      s += tree[k];
      k -= (k & -k);
    }
    return s; 
  }
  void add( i32 k, i64 x ){
    while( k <= n ){
      tree[k] += x;
      k += (k & -k);
    }
  }
  i64 sumq( i32 s ){
    i32 a = inorder[s];
    i32 b = a + size[s] - 1;
    return sum( b ) - sum( a - 1 );
  }
  void changeq( i32 s, i64 x ){
    add( inorder[s], x - v[s] );
    v[s] = x;
  }
  void pusk() {
    cin >> n;
    i32 q; cin >> q;
    for( i32 j = 1; j <= n; ++j ){ cin >> v[j]; }
    for( i32 j = 1; j <  n; ++j ){
      i32 a, b; cin >> a >> b;
      adj[a].push_back( b );
      adj[b].push_back( a );
    }
    dfs( 1, 0 );
    for( i32 j = 1; j <= n; ++j ){
      pref[j] += pref[j - 1];
      tree[j] = pref[j] - pref[j - (j & -j)];
    }    
    while( q-- ){
      i32 type; cin >> type;
      i32 s; cin >> s;
      if( type == 1 ){
        i64 x; cin >> x;
        changeq( s, x );
      } else {
        cout << sumq( s ) << nl;
      }
    }
  }
}
