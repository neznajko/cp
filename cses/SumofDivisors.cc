// 2. Find the shortest sequence of moves that
// transfers a tower of n disks from the left peg A to
// the right peg B, if direct moves between A and B
// are disallowed. (Each move must be to or from the
// middle peg. As usual, a larger disk must never
// appear above a smaller one.)
//
// First we transfer n-1 discs from A to B, then the
// nth disk form A to C, then the n-1 discs from B to
// A, then the nth disk from C to B, and last but not
// least the n-1 disks from A to B, zo
// Tⁿ = 3Tⁿ⁻¹ + 2, now for closed form ve add +1 on
// both sides:
// Tⁿ + 1 = 3Tⁿ⁻¹ + 3 = 3(Tⁿ⁻¹ + 1),
// Uⁿ := Tⁿ + 1,
//
// U₀ = 1
// Uⁿ = 3Uⁿ⁻¹, zo 
// 
// Uⁿ = 3ⁿ, and
// Tⁿ = 3ⁿ - 1
