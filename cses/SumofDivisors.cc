constexpr i64 MOD = 1'000'000'007;
//////////////////////////////////////////////////////
inline i64 mod_mul( i64 a, i64 b ){
  return ((a % MOD) * (b % MOD)) % MOD;
}
inline i64 mod_add( i64 a, i64 b ){
    return (a + b) % MOD;
}
//////////////////////////////////////////////////////
i64 sod( i64 n ){
  i64 a = n; 
  i64 s = 0;
  for( i64 q = 1; q * q <= n; ++q ){
    i64 b = n/(q + 1);
    i64 c = a - b;
    if( c < 2 ){ break; }
    // s += q*( c*b + c*(c+1)/2 );
    i64 S = mod_mul( c, b );
    if( c & 1 ){
      S += mod_mul(( c + 1 ) >> 1, c );
    } else {
      S += mod_mul( c >> 1, c + 1 );
    }
    S = mod_mul( q, S );
    s = mod_add( s, S );
    a = b;
  }
  for(; a > 0; --a ){
    i64 S = n / a * a % MOD;
    s = (s + S) % MOD;
  }
  return s;
}
